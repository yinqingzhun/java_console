package com.yqz.console.tech.model;

public class SerialItem {
	private String sfirstletter;
	private int id;
	private String name;

	public String getSfirstletter() {
		return sfirstletter;
	}

	public void setSfirstletter(String bfirstletter) {
		this.sfirstletter = bfirstletter;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
